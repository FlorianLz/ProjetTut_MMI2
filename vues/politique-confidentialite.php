        <p><strong>9.3.4.</strong> Les Cookies Tiers liés aux boutons  de partage des réseaux sociaux (Cookies Tiers de réseaux sociaux)</p>

        <p>Les Cookies Tiers de réseaux  sociaux permettent de partager le contenu du Site avec des tiers. Tel est,  notamment, le cas pour des boutons « partager » ou « j’aime » émanant de tiers  tel que Facebook ou Twitter. La Société ne contrôle pas ces Cookies Tiers de  réseaux sociaux et les Données collectées par le biais par les sociétés  gestionnaires de ces réseaux sociaux.</p>

