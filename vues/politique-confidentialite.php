        <h3>2 – COMMENT VOS DONNEES SONT-ELLES  COLLECTEES ?</h3>

        <p><strong>2.1.</strong> En  fonction de la façon dont vous interagissez avec la Société, cette dernière est  susceptible de collecter vos Données selon les modalités décrites comme suit.</p>

        <p><strong>2.2.</strong> La  société collecte vos Données directement auprès de vous lorsque notamment  vous remplissez un formulaire de contact ou d’inscription, vous passez une  commande ou encore que vous vous adressez à la Société (par exemple en lui  adressant un courrier électronique). </p>

        <p><strong>2.3. </strong>La  Société collecte indirectement vos Données lorsque vous naviguez sur les  Sites au moyen de cookies et de balises dans les conditions définies plus  avant. La Société collecte aussi vos Données via des tiers dont les réseaux  sociaux lorsque vous utilisez le compte dont vous êtes titulaire auprès de ces  tiers pour vous connecter ou vous inscrire à une manifestation.</p>

        <p><strong>2.4. </strong>Lorsque  vous communiquez des Données à la Société, il vous appartient de vous assurer  qu’elles sont exactes et complètes. Le cas échéant, vous devez les mettre à  jour.</p>

        <p><strong>2.5.</strong> Les  Données présentées comme obligatoires sont nécessaires à la gestion de votre  demande et pour nous permettre de vous fournir les services sollicités. Le  caractère obligatoire ou facultatif des Données est indiqué sur les formulaires  ainsi que les éventuelles conséquences en cas de défaut de réponse.</p>

        <p> </p>

        <h3>3 – POURQUOI VOS DONNEES SONT- ELLES  COLLECTEES ?</h3>

        <p>Vos Données font l’objet de  traitements automatisés mis en œuvre par la Société pour les diverses finalités  ci-après décrites tant dans le cadre de votre utilisation des Sites que de  votre participation aux évènements organisés par la Société ou dont  l’organisation lui a été confiée.</p>

        <p>Vos Données sont traitées  pour gérer les demandes que vous formulez et vous contacter.</p>

        <p><strong>Si vous êtes client,</strong> sur la  base de l’exécution du contrat conclu avec la Société (conditions générales de  pré-enregistrement, conditions générales de vente Billetterie ou conditions  générales de participation), vos Données sont traitées pour gérer vos demandes  de participation et commandes et organiser vos participations et visites sur  les évènements que nous organisons ou dont l’organisation nous a été  confiée. </p>
        <p>Dans une perspective d’amélioration continue des événements qu’elle  organise, vos Données sont aussi traitées par la Société pour vous adresser des  enquêtes post événements et analyser leur résultat.</p>

        <p><strong>Si vous avez la qualité de prospect professionnel</strong> la Société est susceptible de vous adresser, par       tous canaux, ses actualités et informations commerciales sur la base de       son intérêt légitime à promouvoir les événements qu’elle organise.</p>

        <p><strong>Si vous êtes un prospect non professionnel</strong>, la Société est susceptible de vous adresser, par tous       canaux, ses actualités et informations commerciales si vous y avez       consenti.</p>

        <p>Sur la base de votre  consentement, la Société peut également vous adresser, par tous canaux, des  actualités et informations commerciales concernant les événements organisés par  d’autres entités du Groupe Parlons Harcèlement et/ou les services de partenaires.</p>

        <h3>4 – A QUI VOS DONNEES SONT-ELLES  DESTINEES ?</h3>

        <p><strong>4.1.</strong> Vos  Données sont traitées par les équipes internes de la Société. En outre, seuls  les prestataires de service et leur personnel limitativement habilités sont  autorisés à accéder à vos Données et ce, eu égard aux prestations spécifiques  qui leur sont confiées et qu’ils doivent réaliser pour le compte exclusif de la  Société dans le strict respect d’obligations notamment de sécurité et de  confidentialité.</p>

        <p><strong>4.2.</strong> Avec  votre consentement lorsqu’il est requis, vos Données sont susceptibles d’être  transmises aux entités du Groupe auquel la Société appartient ainsi qu’à des  partenaires notamment afin qu’ils vous adressent des informations et actualités  sur les services et produits qu’ils proposent.</p>

        <p><strong>4.3.</strong> Lorsque  la réglementation en vigueur l’exige, la Société est susceptible de transmettre  vos Données aux organismes et autorités légalement autorisés à y accéder  (notamment les autorités judiciaires et administratives).</p>

        <h3>5 – COMMENT  VOS DONNEES SONT-ELLES PROTEGEES ?</h3>

        <p>La Société prend toutes  précautions utiles afin de préserver la sécurité et la confidentialité de vos  Données afin, notamment, d’empêcher qu'elles soient déformées, endommagées,  divulguées et que des tiers non-autorisés y aient accès.</p>

