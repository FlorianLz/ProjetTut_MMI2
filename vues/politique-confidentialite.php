        <h3>9 – COOKIES</h3>

        <p>Vous êtes informé que, lors de  vos visites sur le Site, un ou plusieurs Cookies peuvent être installés sur  votre terminal.</p>

        <h4>9.1. Qu’est-ce qu’un Cookie ?</h4>

        <p>Au sens de la présente  Politique, le Cookie désigne l’ensemble des formes d’accès et d’inscription  d’informations sur votre terminal et notamment les informations envoyées par  les Sites et stockées par votre navigateur sur un espace dédié du disque dur dudit  terminal. </p>

        <p> </p>

        <h4><strong>9.2. </strong>Pourquoi et comment les Cookies sont-ils  utilisés ?</h4>

        <p><strong>9.2.1.</strong> La  Société utilise des Cookies pour :</p>

        <ul>
            <p>assurer le fonctionnement et optimiser la performance       des Sites,</p>
            <p>adapter les Sites à vos préférences. A titre d’exemple,       le Site s’adapte aux préférences d’affichage de votre terminal notamment       en tenant compte de la langue utilisée ou encore la résolution       d’affichage.</p>
            <p>faciliter votre navigation notamment en vous évitant       d’avoir à ressaisir des informations à chaque visite sur les Sites,</p>
            <p>réaliser la mesure d’audience des Sites et des       communications qui vous sont adressées,</p>
            <p>réaliser de la publicité ciblée adaptée à vos centres       d’intérêts,</p>
            <p>développer l’interactivité des Sites.</p>
        </ul>

        <p><strong>9.2.2.</strong> Vous  êtes également informé que la Société permet à des tiers de déposer des Cookies  via les Sites. La Société attire votre attention sur le fait que ces Cookies  sont uniquement et seulement exploités par ces tiers.    </p>

        <p> </p>

        <h4><strong>9.3. </strong>Quels sont les types de Cookies utilisés ?</h4>

        <p><strong>9.3.1 </strong>Les Cookies Techniques</p>

        <p>Les Cookies Techniques ont pour  finalité de faciliter la navigation sur le Site, de permettre et d’améliorer le  fonctionnement des Sites ainsi que l’accès aux différentes fonctionnalités des  Sites. <br>  Ils permettent également de mettre en œuvre des mesures de sécurité. Par exemple,  lorsque cela vous est applicable, en vous demandant de vous connecter à nouveau  à l’espace exposant après un certain laps de temps.</p>

        <p>Vous êtes informé que si vous  choisissez de désactiver ces Cookies Techniques, l’accès aux services pourrait  alors se révéler altéré. Dans ce contexte, la responsabilité de la Société ne  saurait être engagée à aucun titre.</p>

        <p><strong>9.3.2. </strong>Les Cookies de Mesure d’Audience</p>

        <p>Les Cookies de Mesure  d’Audience permettent de mesurer la fréquentation des Sites et des  communications qui vous sont adressées (contenus, rubriques, temps passé,  navigateur utilisé…).</p>

        <p>Vous êtes informé que ces  Cookies de Mesure d’Audience peuvent, le cas échéant, être déposés sur les  Sites par des tiers pour le compte de la Société.</p>

        <p><strong>9.3.3.</strong> Les  Cookies Tiers de publicité ciblée (Cookies Tiers publicitaires)</p>

        <p>Les Cookies Tiers publicitaires  permettent de proposer des publicités adaptées à vos centres d’intérêts. Ils  sont déposés par la Société ou par des tiers soit pour le compte de la Société,  soit pour leur propre compte.</p>

