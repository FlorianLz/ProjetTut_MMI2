        <h1 class="titrepage text-center">Politique de confidentialité</h1>

        <p>Le  Groupe Parlons Harcèlement apporte une attention particulière à la protection des  données à caractère personnel. Il s’agit d’une condition essentielle de la  relation de confiance que le Groupe Parlons Harcèlement souhaite établir avec tous ceux  qui lui confient leurs données à caractère personnel.</p>

        <p>La présente politique de  protection des Données à caractère personnel (Ci-après la « <strong>Politique </strong>»)  énonce les pratiques et les conditions dans lesquelles la société [société organisatrice de  l’événement considéré] (Ci-après la « <strong>Société </strong>»)  traite vos Données à caractère personnel.</p>

        <p>La présente Politique  s’applique lorsque vos Données sont collectées sur les sites Internet édités  par les entités du Groupe Parlons Harcèlement (Ci-après les « <strong>Sites</strong> ») ainsi  que dans le cadre vos relations, de quelque nature qu’elles soient, avec la  Société.</p>

        <p>Les Données à caractère  personnel (ci-après les « <strong>Données </strong>»)  sont toute information se rapportant à une personne physique identifiée ou  identifiable ; est réputée être une «personne physique identifiable» une  personne physique qui peut être identifiée, directement ou indirectement,  notamment par référence à un identifiant, tel qu'un nom, un numéro  d'identification, des données de localisation, un identifiant en ligne, ou à un  ou plusieurs éléments spécifiques propres à son identité physique,  physiologique, génétique, psychique, économique, culturelle ou sociale ;  tel que ce terme est défini par la réglementation en vigueur.</p>

        <p> </p>

        <h3>1 – QUELLES SONT LES DONNEES COLLECTEES ?</h3>

        <p>En fonction de la façon dont  vous interagissez avec la Société et de la finalité de cette interaction, la  Société est susceptible de collecter les Données suivantes :</p>

        <ul>
            <p><strong>Donnés relatives à votre identité :</strong> ces Données désignent notamment votre nom, votre       prénom, votre adresse électronique, votre adresse postale ou encore votre       numéro de téléphone et votre profil (candidat, visiteur, exposant…).</p>

            <p><strong>Données relatives à votre commande :</strong> ces Données désignent l’ensemble des informations       nécessaires à la satisfaction et à la complétude de votre commande,       notamment les informations relatives à la carte bancaire utilisée (nom du       titulaire de la carte, numéro de la carte, date d’expiration…), le détail       de la commande, la date de la commande, les échanges avec le service des       relations commerciales. En tout état de cause, les Données relatives au       paiement sont traitées conformément à la réglementation et aux normes de       sécurité applicable en la matière. </p>
            <p><strong>Données relatives à la connexion et à l’utilisation du       Compte lorsque vous en est titulaire :</strong> ces Données désignent l’ensemble des informations       dont vous avez besoin pour accéder à votre espace personnel ainsi que       celles relatives à l’utilisation du Compte Client et notamment, vos       identifiant et mot de passe, vos choix et préférences, l’historique des       évènements auxquels vous avez participé. En tout état de cause, le Compte       Client fait l’objet de conditions générales d’utilisation spécifique. </p>
            <p><strong>Données relatives à la navigation : </strong>ces Données désignent elles concernent la manière       dont l’Utilisateur navigue sur le Site. Ces Données peuvent inclure       l’adresse IP, la résolution de l’écran, le navigateur utilisé, le temps de       navigation, historique de recherche, le système d’exploitation utilisé, la       langue et les pages visualisées.</p>
            <p><strong>Données relatives à votre participation aux salons :</strong> date de participation, stands visités, achats       réalisés.</p>
            <p><strong>Données relatives à l’utilisation des réseaux       sociaux :</strong> lorsque       vous interagissez avec les fonctionnalités de réseaux sociaux.</p>
        </ul>

